TOPICS_MAP = {
    "жкх": "ЖКХ",
    "лиф": "ЖКХ",
    "вода": "ЖКХ",
    "дорог": "Транспорт",
    "дорога": "Транспорт",
    "автобус": "Транспорт",
    "школ": "Социальная сфера",
    "сад": "Социальная сфера",
    "парк": "Благоустройство",
    "лавоч": "Благоустройство"
}

def classify(text, title=None):
    combined = (title or "") + " " + (text or "")
    s = combined.lower()
    for k, dept in TOPICS_MAP.items():
        if k in s:
            return dept
    return "Другое"